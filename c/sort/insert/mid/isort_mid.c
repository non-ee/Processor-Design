// convert number to string. return string size
int numTostr(char *s, int n, int code)
{
  int i;
  char tmp;
  char *s_orig = s;

  for (i = code; i <= n; i = i * code)
    ;

  for (i = i / code; i != 0; i = i / code)
  {
    tmp = (char)(n / i);
    n = n % i;
    if (tmp < 10)
    {
      *(s++) = tmp + '0';
    }
    else
    {
      *(s++) = tmp - 10 + 'a';
    }
  }

  *s = '\0';

  return (int)(s - s_orig);
}

